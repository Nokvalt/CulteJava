package c.culte.request;

public class CulteRequest {
	
	private double argent;
	private int effectif;
	private int tapoteurId;
	private String tapoteurNom;
	public double getArgent() {
		return argent;
	}
	public void setArgent(double argent) {
		this.argent = argent;
	}
	public int getEffectif() {
		return effectif;
	}
	public void setEffectif(int effectif) {
		this.effectif = effectif;
	}
	public int getTapoteurId() {
		return tapoteurId;
	}
	public void setTapoteurId(int tapoteurId) {
		this.tapoteurId = tapoteurId;
	}
	public String getTapoteurNom() {
		return tapoteurNom;
	}
	public void setTapoteurNom(String tapoteurNom) {
		this.tapoteurNom = tapoteurNom;
	}
	
	

}
