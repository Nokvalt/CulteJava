package c.culte.request;

public class ConnexionRequest {
	String login;
	String password;
	
	public String getLogin() {
		return login;
	}
	public void setLogin(String login) {
		this.login = login;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String mdp) {
		this.password = mdp;
	}
	
	
}
